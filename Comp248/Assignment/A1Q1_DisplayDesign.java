public class A1Q1_DisplayDesign {

	public static void main(String[] args) {
		
	    //Using an escape sequence whenever we need to type a backslash.
		//Display each line by "System.out.println".
		
		System.out.println("  \\/  ");
		System.out.println(" \\\\// ");
		System.out.println("\\\\\\///");
		System.out.println("///\\\\\\");
		System.out.println(" //\\\\");
		System.out.println("  /\\  ");
		
		System.out.println("\nAll done!"); //Jump to next line and print out "All done!"
	}

}
